const mongoose = require('../database');

const ServiceSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    }
});

const Service = mongoose.model('Service', ServiceSchema);

module.exports = Service;
